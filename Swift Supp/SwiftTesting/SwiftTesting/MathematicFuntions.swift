import Foundation

class MathematicFunctions{
    
    func addIntegers(x:Int,y:Int)->Int{
        return x+y
    }
    func difIntegers(x:Int,y:Int)->Int{
        return x-y
    }
    func multiIntegers(x:Int,y:Int)->Int{
        return x*y
    }
    func divideIntegers(x:Int,y:Int)->Int{
        return x/y
    }
}
